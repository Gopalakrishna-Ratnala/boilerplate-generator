function enableZonelessForLegacyVersion(projectDir, targetAngularMajor) {
  // v21+ is zoneless by default already (confirmed via real testing across v19/v20/v21:
  // v21's own `ng new` output has no explicit zoneless provider at all — Angular's
  // default scheduling itself changed at v21, not just the scaffolding). v19/v20 need
  // this done explicitly and correctly, not just "remove zone.js and hope."
  if (targetAngularMajor >= 21) return;

  info(`\n⚡ Angular ${targetAngularMajor} does not default to zoneless — enabling explicitly...`);

  // provideZonelessChangeDetection() was still experimental (different name) at v19,
  // stabilized at v20.2. Verified via angular.dev's own API docs and real package
  // behavior, not assumed.
  const providerName =
    targetAngularMajor === 19 ? 'provideExperimentalZonelessChangeDetection' : 'provideZonelessChangeDetection';

  // 1. Remove zone.js from angular.json polyfills — build AND test targets both have
  // it by default (confirmed via real inspection), and both need it removed.
  const angularJsonPath = path.join(projectDir, 'angular.json');
  const angularJson = JSON.parse(fs.readFileSync(angularJsonPath, 'utf8'));
  const projectKey = Object.keys(angularJson.projects)[0];
  const architect = angularJson.projects[projectKey].architect;
  for (const target of ['build', 'test']) {
    const polyfills = architect?.[target]?.options?.polyfills;
    if (Array.isArray(polyfills)) {
      architect[target].options.polyfills = polyfills.filter((p) => p !== 'zone.js' && p !== 'zone.js/testing');
    }
  }
  fs.writeFileSync(angularJsonPath, JSON.stringify(angularJson, null, 2) + '\n');

  // 2. Remove zone.js from package.json dependencies.
  const pkgPath = path.join(projectDir, 'package.json');
  const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
  if (pkg.dependencies) delete pkg.dependencies['zone.js'];
  fs.writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n');

  // 3. Replace the existing provideZoneChangeDetection({ eventCoalescing: true }) call
  // with the correct zoneless provider — both versions scaffold this call by default
  // (confirmed via real generation), and leaving it alongside a zoneless provider would
  // be contradictory. Using literal string replacement, not regex splicing into the
  // providers array — real testing showed v19 formats the array on one line and v20/v21
  // use multi-line, so a format-agnostic approach is safer than assuming line structure.
  const appConfigPath = path.join(projectDir, 'src', 'app', 'app.config.ts');
  if (fs.existsSync(appConfigPath)) {
    let content = fs.readFileSync(appConfigPath, 'utf8');
    const oldCall = 'provideZoneChangeDetection({ eventCoalescing: true })';
    if (content.includes(oldCall)) {
      content = content.split(oldCall).join(`${providerName}()`);
      // The import statement lists the bare identifier separately from the call —
      // replace it too now that the call-site occurrence is already handled above.
      content = content.split('provideZoneChangeDetection').join(providerName);
      fs.writeFileSync(appConfigPath, content);
      info(`   ✓ Replaced provideZoneChangeDetection with ${providerName}() in app.config.ts.`);
    } else {
      warn(
        `Expected to find 'provideZoneChangeDetection({ eventCoalescing: true })' in ` +
          `app.config.ts for Angular ${targetAngularMajor} but didn't — the CLI's scaffolded ` +
          'output may have changed. Zoneless was NOT enabled; flag for review.'
      );
    }
  }

  info(`   ✓ Removed zone.js from angular.json polyfills (build + test) and package.json.`);

  // 4. The CLI-generated root component spec file bootstraps its own TestBed module,
  // separately from app.config.ts — it does NOT inherit the zoneless provider just
  // added there. Left unfixed, `ng test` throws NG0908 ("this configuration requires
  // Zone.js") because the test injector falls back to expecting zone.js, which no
  // longer exists in this project. This was NOT caught by this project's own sandbox
  // testing (no Chrome binary available to actually run Karma) — found via a real
  // `npm test` run on a real machine, during the user's own verification pass. Same
  // dual-candidate-filename handling as the root component fix above (v19:
  // app.component.spec.ts, v20+: app.spec.ts).
  for (const candidateName of ['app.spec.ts', 'app.component.spec.ts']) {
    const specPath = path.join(projectDir, 'src', 'app', candidateName);
    if (!fs.existsSync(specPath)) continue;

    let specContent = fs.readFileSync(specPath, 'utf8');
    const oldModuleConfig = /TestBed\.configureTestingModule\(\{\s*\n(\s*)imports: (\[[^\]]*\]),\s*\n\s*\}\)/;
    const match = specContent.match(oldModuleConfig);

    if (match) {
      const [fullMatch, indent, importsValue] = match;
      const replacement =
        `TestBed.configureTestingModule({\n${indent}imports: ${importsValue},\n${indent}providers: [${providerName}()],\n  })`;
      specContent = specContent.replace(fullMatch, replacement);
      // Add the import — the spec file has no existing @angular/core import to merge
      // into (only '@angular/core/testing' and the component import), so add a new line.
      specContent = `import { ${providerName} } from '@angular/core';\n` + specContent;
      fs.writeFileSync(specPath, specContent);
      info(`   ✓ Added ${providerName}() to ${candidateName}'s TestBed providers (fixes NG0908).`);
    } else {
      warn(
        `Expected to find TestBed.configureTestingModule({ imports: [...] }) in ` +
          `${candidateName} but the pattern didn't match — the CLI's scaffolded spec file ` +
          'may have changed. This project\'s ng test will likely fail with NG0908; flag for review.'
      );
    }
  }
}

