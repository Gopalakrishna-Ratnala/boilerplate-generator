#!/usr/bin/env node
'use strict';

/**
 * generate.js — deterministic assembly of a new Angular project from base/ + bundles/.
 *
 * This script does NOT use AI. Given the same flags, it produces the same output every
 * time. A Claude Code skill is expected to sit in front of this and handle the
 * conversational Q&A, then call this script with plain flags — see CONTEXT.md §2 for
 * why that split exists.
 *
 * Usage:
 *   node generate.js \
 *     --project-name=my-app \
 *     --auth=basic-auth \
 *     --data-layer=rest \
 *     --state=signals-only \
 *     --roles=single-role \
 *     --deploy-target=spa \
 *     --repo=https://github.com/org/my-app.git \
 *     [--out-dir=/path/to/parent/dir] \
 *     [--description="One line description"] \
 *     [--selector-prefix=myapp] \
 *     [--angular-version=22] \
 *     [--package-manager=npm] \
 *     [--dry-run=true]
 *
 * Requires on PATH: node, npm, git, jq (jq is a runtime dependency of the generated
 * project's hooks, checked here so a broken generated repo isn't produced silently).
 * Requires GITHUB_TOKEN in the environment if --repo is provided.
 */

const fs = require('fs');
const os = require('os');
const path = require('path');
const { spawnSync } = require('child_process');

const REPO_ROOT = path.resolve(__dirname, '..');
const BASE_DIR = path.join(REPO_ROOT, 'base');
const BUNDLES_DIR = path.join(REPO_ROOT, 'bundles');

const AXES = ['auth', 'data-layer', 'state', 'roles', 'deploy-target', 'i18n', 'offline', 'styling', 'e2e'];

// Used only when --angular-version isn't specified (meaning "use whatever `ng new`
// resolves as latest"). This must be kept updated as Angular ships new majors —
// it's used purely for version-constraint validation (minAngularMajor/maxAngularMajor),
// not for anything that affects what actually gets installed.
const ASSUMED_LATEST_ANGULAR_MAJOR = 22;

function resolveAngularMajor(angularVersionArg) {
  if (!angularVersionArg) return ASSUMED_LATEST_ANGULAR_MAJOR;
  const match = String(angularVersionArg).match(/^(\d+)/);
  if (!match) {
    fail(`Could not parse a major version number from --angular-version="${angularVersionArg}".`);
  }
  return parseInt(match[1], 10);
}

// Commands that run for EVERY generated project, regardless of bundle selection —
// company-wide standards, not per-client decisions. Contrast with a bundle's
// `postGenerateCommands`, which only runs when that specific option is selected.
// Added after discovering `ng new` no longer scaffolds ESLint by default (as of the
// Angular CLI version tested against) — CLAUDE.md already instructs the agent to run
// `ng lint`, so without this, every generated project would ship a false promise and a
// non-functional command.
// Added `ng generate environments` after discovering — via a real `ng build` (not just
// `ng lint`/`ng test`) — that `ng new` hasn't scaffolded src/environments/ by default
// since Angular 15. Every data-layer bundle's config file (api.config.ts,
// graphql.config.ts, realtime.config.ts) imports from '../../../environments/environment',
// meaning every project generated by this system before this fix would fail its
// production build. See CONTEXT.md for the full account.
// Pins @angular-eslint/schematics to the SAME major version as the target Angular
// version, rather than leaving it unpinned. Found via research (not assumed): there is
// a documented, filed bug (angular-eslint#2180) where `ng add @angular-eslint/schematics`
// unpinned can fail to resolve the version matching a non-latest target Angular version
// and instead grab the latest regardless — silently installing a mismatched major
// version. @angular-eslint's major version is guaranteed to align 1:1 with Angular's own
// major version (documented, stable policy since Angular v12), so pinning explicitly is
// both safe and necessary for anything other than the true latest.
function getBasePostGenerateCommands(targetAngularMajor) {
  return [
    `ng add @angular-eslint/schematics@${targetAngularMajor} --skip-confirmation`,
    'ng generate environments',
  ];
}

// ---------------------------------------------------------------------------
// Small utilities
// ---------------------------------------------------------------------------

function fail(msg) {
  console.error(`\n❌ ${msg}\n`);
  process.exit(1);
}

function warn(msg) {
  console.warn(`\n⚠️  ${msg}`);
}

function info(msg) {
  console.log(msg);
}

function parseArgs(argv) {
  const args = {};
  for (const raw of argv.slice(2)) {
    const m = raw.match(/^--([^=]+)=([\s\S]*)$/);
    if (m) {
      args[m[1]] = m[2];
    } else if (raw.startsWith('--')) {
      args[raw.slice(2)] = 'true';
    }
  }
  return args;
}

function commandExists(cmd) {
  const result = spawnSync(cmd, ['--version'], { stdio: 'ignore', shell: process.platform === 'win32' });
  return result.status === 0;
}

function run(cmd, args, opts = {}) {
  const result = spawnSync(cmd, args, { stdio: 'inherit', ...opts });
  return result.status === 0;
}

function runCapture(cmd, args, opts = {}) {
  return spawnSync(cmd, args, { stdio: 'pipe', encoding: 'utf8', ...opts });
}

