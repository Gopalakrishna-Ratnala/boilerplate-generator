function fillPlaceholders(content, values) {
  let out = content;
  for (const [key, value] of Object.entries(values)) {
    out = out.split(`{{${key}}}`).join(value);
  }
  return out;
}

function copyDirRecursive(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const s = path.join(src, entry.name);
    const d = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      copyDirRecursive(s, d);
    } else {
      fs.copyFileSync(s, d);
    }
  }
}

function wireHttpClientAndInterceptors(projectDir, manifests) {
  // CRITICAL FIX, found via a real tester's report (session 24, test-reports/tester-3):
  // provideHttpClient() was NEVER added to app.config.ts by anything in this system —
  // not base, not any bundle's postGenerateCommands. But basic-auth/rest/graphql/saml
  // all generate code (ApiService, AuthService, interceptors) that assumes HttpClient
  // is provided. Every project using one of these would throw a real
  // NullInjectorError the moment it actually ran in a browser — completely invisible
  // to `ng lint`, `ng build`, and unit tests, since tests always supply their own
  // provideHttpClient()/provideHttpClientTesting() in TestBed, masking the real app's
  // brokenness. Only found because a tester manually wired PrimeNG (a *different*,
  // correctly-documented "manual wiring required" bundle) and happened to notice
  // HttpClient was ALSO silently missing while looking closely at app.config.ts.
  //
  // The `needsHttpClient`/`httpInterceptors` manifest fields already existed on
  // several bundles (basic-auth, rest, saml, graphql) before this fix — the data model
  // was already correct, but nothing in generate.js ever actually read it. This
  // function is what was missing, not new bundle-level design.
  const needsHttpClient = AXES.some((axis) => manifests[axis].needsHttpClient);
  if (!needsHttpClient) return;

  const interceptors = [];
  for (const axis of AXES) {
    if (manifests[axis].httpInterceptors) {
      interceptors.push(...manifests[axis].httpInterceptors);
    }
  }

  const appConfigPath = path.join(projectDir, 'src', 'app', 'app.config.ts');
  if (!fs.existsSync(appConfigPath)) {
    warn('app.config.ts not found — skipping HttpClient wiring. Flag for review.');
    return;
  }

  let content = fs.readFileSync(appConfigPath, 'utf8');

  // Idempotency guard — if this has somehow already been wired (shouldn't happen in
  // the normal pipeline, but defensive), don't double-wire it.
  if (content.includes('provideHttpClient')) {
    return;
  }

  const httpClientImport =
    interceptors.length > 0
      ? "import { provideHttpClient, withInterceptors } from '@angular/common/http';\n"
      : "import { provideHttpClient } from '@angular/common/http';\n";
  const interceptorImports = interceptors.map((i) => `import { ${i.importName} } from '${i.importFrom}';\n`).join('');

  // Prepend new imports at the very top — always valid regardless of whatever import
  // structure already exists in the file (single-line, multi-group, etc.).
  content = httpClientImport + interceptorImports + content;

  const providerCall =
    interceptors.length > 0
      ? `provideHttpClient(withInterceptors([${interceptors.map((i) => i.importName).join(', ')}])),`
      : 'provideHttpClient(),';

  const providersMatch = content.match(/providers:\s*\[/);
  if (!providersMatch) {
    warn('Could not find a providers array in app.config.ts — skipping HttpClient wiring. Flag for review.');
    return;
  }
  const insertPos = providersMatch.index + providersMatch[0].length;
  content = content.slice(0, insertPos) + `\n    ${providerCall}` + content.slice(insertPos);

  fs.writeFileSync(appConfigPath, content);
  info(
    `   ✓ Wired provideHttpClient()${
      interceptors.length ? ' with interceptors: ' + interceptors.map((i) => i.importName).join(', ') : ''
    } into app.config.ts (was completely missing before this fix — see CONTEXT.md).`
  );
}

function fixRoleDirectiveSelector(projectDir, selectorPrefix, rolesSelection) {
  // CRITICAL FIX, found via a real tester's report (session 26, test-reports/tester-5):
  // the rbac bundle's has-role.directive.ts ships with selector: '[hasRole]' — no
  // prefix at all. @angular-eslint's directive-selector rule requires every custom
  // directive's selector to start with this project's configured prefix, so this
  // fails `ng lint` on EVERY rbac project out of the box — and since the generator's
  // own initial commit runs `eslint --fix` via the pre-commit hook, this also breaks
  // the generator's own bootstrap commit for every rbac project. Not an artifact of
  // any specific --selector-prefix value either — '[hasRole]' has no prefix even
  // against Angular's own default 'app' prefix.
  //
  // Unlike the root component's selector (a simple string, no corresponding property),
  // this directive uses Angular's structural-directive microsyntax, where
  // `*directiveName="value"` requires an input literally named `directiveName` — so
  // fixing this requires renaming the input property and its internal usage too, not
  // just the selector string.
  if (rolesSelection !== 'rbac') return;

  const directivePath = path.join(projectDir, 'src', 'app', 'core', 'directives', 'has-role.directive.ts');
  if (!fs.existsSync(directivePath)) {
    warn('rbac selected but has-role.directive.ts not found — skipping directive-selector fix. Flag for review.');
    return;
  }

  // Sanitize to a strict camelCase-safe token — selectorPrefix itself is only
  // guaranteed safe for kebab-case component selectors (used as-is elsewhere), not
  // necessarily already lowercase/alphanumeric-only.
  const camelPrefix = selectorPrefix.toLowerCase().replace(/[^a-z0-9]/g, '') || 'app';
  const newName = `${camelPrefix}HasRole`;

  let content = fs.readFileSync(directivePath, 'utf8');
  content = content.split("selector: '[hasRole]'").join(`selector: '[${newName}]'`);
  content = content.split('readonly hasRole = input').join(`readonly ${newName} = input`);
  content = content.split('this.hasRole()').join(`this.${newName}()`);
  content = content.split('*hasRole=').join(`*${newName}=`); // the file's own usage-example comment
  fs.writeFileSync(directivePath, content);

  // roles.md documents the same usage pattern — keep it consistent, not stale.
  const rolesRulePath = path.join(projectDir, '.claude', 'rules', 'roles.md');
  if (fs.existsSync(rolesRulePath)) {
    let rulesContent = fs.readFileSync(rolesRulePath, 'utf8');
    rulesContent = rulesContent.split('*hasRole').join(`*${newName}`);
    fs.writeFileSync(rolesRulePath, rulesContent);
  }

  info(`   ✓ Renamed has-role directive selector to [${newName}] (was unprefixed '[hasRole]', which failed lint on every rbac project).`);
}

function applyBase(projectDir, placeholderValues) {
  info('\n🛡  Applying base .claude guardrail layer...');

  const claudeMdSrc = fs.readFileSync(path.join(BASE_DIR, 'CLAUDE.md'), 'utf8');
  fs.writeFileSync(path.join(projectDir, 'CLAUDE.md'), fillPlaceholders(claudeMdSrc, placeholderValues));

  copyDirRecursive(path.join(BASE_DIR, '.claude'), path.join(projectDir, '.claude'));

  const mcpConfigSrc = path.join(BASE_DIR, '.mcp.json');
  if (fs.existsSync(mcpConfigSrc)) {
    fs.copyFileSync(mcpConfigSrc, path.join(projectDir, '.mcp.json'));
  }

  // Apply placeholder substitution to EVERY file in .claude/rules/, not a hand-picked
  // list. Previously only CLAUDE.md and architecture.md were processed here, which
  // meant a new placeholder added to any other rule file (like {{TEST_RUNNER}} in
  // angular.md) would silently never get filled in. fillPlaceholders() is a no-op for
  // files with no matching tokens, so processing every file is safe.
  const rulesDir = path.join(projectDir, '.claude', 'rules');
  for (const ruleFile of fs.readdirSync(rulesDir)) {
    const ruleFilePath = path.join(rulesDir, ruleFile);
    if (fs.statSync(ruleFilePath).isFile()) {
      fs.writeFileSync(ruleFilePath, fillPlaceholders(fs.readFileSync(ruleFilePath, 'utf8'), placeholderValues));
    }
  }

  // Hooks must be executable — permissions aren't guaranteed to survive a plain copy.
  const hooksDir = path.join(projectDir, '.claude', 'hooks');
  for (const f of fs.readdirSync(hooksDir)) {
    fs.chmodSync(path.join(hooksDir, f), 0o755);
  }

  info('   ✓ CLAUDE.md, .claude/rules/, .claude/settings.json, .claude/hooks/, .mcp.json in place.');
}

